def _page_url(base_url: str, pageno: int) -> str:
    return f"{base_url}/projects/{_PROJECT_ID}/pages/pageno/{pageno}"


def _goto_project_page(page: Page, base_url: str, pageno: int, *, timeout: int = 20_000) -> None:
    """Navigate to the project page and wait for the shell to mount."""
    page.goto(_page_url(base_url, pageno), timeout=timeout)
    page.wait_for_selector('[data-testid="project-page"]', timeout=timeout)


def _wait_for_line_cards(page: Page, *, min_count: int = 1, timeout: int = 15_000) -> int:
    """Wait until at least ``min_count`` worklist rows are visible. Returns count.

    The ``WordMatchView`` (``line-card-*`` testids) uses a virtualizer that
    renders zero items when its container is ``display:none``.  The Drawer
    worklist (``worklist-row-*``) is always visible and uses no virtualizer,
    making it a reliable proxy for "the server returned line data for this page".

    Note: the worklist default filter is "unvalidated"; on page 1 the fixture
    pre-validates line 0, so the worklist shows lines 1–3 (3 rows).  The
    minimum expectation is therefore ``>= 1`` regardless of page.
    """
    try:
        page.wait_for_selector('[data-testid^="worklist-row-"]', timeout=timeout)
        return page.locator('[data-testid^="worklist-row-"]').count()
    except Exception:
        return 0



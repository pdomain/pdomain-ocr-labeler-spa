"""End-to-end exercise harness against a realistic multi-page project.

Exercises the major UI paths across 8 pages of the ``exercise-fixture``
project, which contains pre-built UserPageEnvelope files with real
block/paragraph/line/word structures (generated by
``scripts/generate_exercise_fixture.py``).

Unlike the conformance tests (``test_driver_contract.py``) and smoke tests
(``test_smoke.py``), this module is intentionally *not* a unit-by-unit
pass/fail matrix — it is a **workflow exercise** that visits real UI paths
in sequence and records failures without aborting.  A single page failure
does not stop the exercise; all results are reported at the end.

Generate the fixture first (idempotent)::

    cd /workspaces/ocr-container/pd-ocr-labeler-spa
    uv run python scripts/generate_exercise_fixture.py

Then run::

    make exercise-real
    # or for headed mode:
    HEADED=1 make exercise-real
    # or directly:
    uv run --group e2e pytest tests/e2e/exercise_real_project.py -v

Spec ref: ``docs/architecture/13-driver-contract.md`` §2 (testid catalogue).
"""


def _pick_free_port() -> int:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


def _spa_built() -> bool:
    static = Path(__file__).resolve().parents[2] / "src" / "pdomain_ocr_labeler_spa" / "static"
    return (static / "index.html").is_file()


def _wait_until(url: str, timeout: float = 20.0) -> None:
    deadline = time.monotonic() + timeout
    while time.monotonic() < deadline:
        try:
            r = httpx.get(url, timeout=0.5)
            if r.status_code == 200:
                return
        except httpx.HTTPError:
            pass
        time.sleep(0.1)
    raise RuntimeError(f"Server did not become ready at {url!r} within {timeout}s")


@dataclass
class ExerciseServer:
    base_url: str
    settings: Settings
    source_root: Path
    data_root: Path


@pytest.fixture(scope="module")
def exercise_server(tmp_path_factory: pytest.TempPathFactory) -> Iterator[ExerciseServer]:
    """Start a live server seeded with the exercise-fixture project.

    Skips when the SPA bundle is not present or the fixture source is missing.
    Session-scoped at *module* level so the 8-page exercise shares one server
    and preserves any server-side state mutations (validate, save, etc.) across
    tests within this module.
    """
    if not _spa_built():
        pytest.skip("SPA not built — run `make frontend-build` (or `make e2e`) first")

    if not _EXERCISE_FIXTURE_SRC.exists():
        pytest.skip("Exercise fixture missing — run: uv run python scripts/generate_exercise_fixture.py")

    data_root = tmp_path_factory.mktemp("exercise-data")
    cache_root = tmp_path_factory.mktemp("exercise-cache")
    config_root = tmp_path_factory.mktemp("exercise-config")
    source_root = tmp_path_factory.mktemp("exercise-source")

    # Copy the exercise-fixture project into source_root.
    dest = source_root / _PROJECT_ID
    shutil.copytree(_EXERCISE_FIXTURE_SRC, dest)

    # Seed the event store (LabelerPageStore) from the committed labeled
    # envelopes so ``load_labeled`` reconstructs each page's real OCR content
    # without running OCR. The store lives under the project dir (``dest``);
    # we close it after seeding so the server opens its own instance.
    #
    # NOTE: the prior approach — copying page-images JSON into
    # ``data_root/labeled-projects/`` — predates the event-store ``load_labeled``
    # lane (M5b) and silently no-ops: ``load_labeled`` resolves the project
    # aggregate from the event store, so flat JSON files were never read and
    # every page fell through to (empty) cold OCR. This mirrors the proven
    # ``_seed_event_store`` path in ``test_selection_operations_parity.py``.
    store = LabelerPageStore(dest)
    try:
        for env_src in sorted((_EXERCISE_FIXTURE_SRC / "page-images").glob("*.json")):
            envelope = json.loads(env_src.read_text())
            source = envelope["source"]
            page_index = source["page_index"]
            image_bytes = (dest / source["image_path"]).read_bytes()
            book_page = BookPage.from_dict(envelope["payload"]["page"])
            _ingest_ocr_result(
                page=book_page,
                image_bytes=image_bytes,
                page_index=page_index,
                store=store,
            )
            _register_page_in_project(
                store=store,
                project_id=_PROJECT_ID,
                page_id=book_page.page_id,
                page_index=page_index,
            )
    finally:
        store.close()

    port = _pick_free_port()
    settings = Settings(
        host="127.0.0.1",
        port=port,
        data_root=data_root,
        cache_root=cache_root,
        config_root=config_root,
        source_projects_root=source_root,
        mode="normal",
    )

    app = build_app(settings)
    import uvicorn

    config = uvicorn.Config(app, host=settings.host, port=settings.port, log_level="warning")
    server = uvicorn.Server(config)
    thread = threading.Thread(target=server.run, daemon=True)
    thread.start()

    base_url = f"http://{settings.host}:{settings.port}"
    try:
        _wait_until(f"{base_url}/healthz")
    except RuntimeError:
        server.should_exit = True
        thread.join(timeout=2)
        raise

    # Load the exercise project into the server's session state.
    r = httpx.post(
        f"{base_url}/api/projects/source-root",
        json={"path": str(source_root)},
        timeout=SEED_TIMEOUT,
    )
    assert r.status_code in (200, 204), f"source-root POST failed: {r.status_code} {r.text}"

    r = httpx.post(
        f"{base_url}/api/projects/load",
        json={"project_root": str(dest)},
        timeout=SEED_TIMEOUT,
    )
    assert r.status_code == 200, f"load project failed: {r.status_code} {r.text}"

    yield ExerciseServer(
        base_url=base_url,
        settings=settings,
        source_root=source_root,
        data_root=data_root,
    )

    server.should_exit = True
    thread.join(timeout=5)


